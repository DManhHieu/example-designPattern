package structural.bridge;

public class Movie {
    private String title;
    private String duration;
    private String kind;
    private String year;
    public Movie(){

    }

    public String getTitle() {
        return title;
    }

    public String getDuration() {
        return duration;
    }

    public String getKind() {
        return kind;
    }

    public String getYear() {
        return year;
    }
}
